"use strict";
//-----------------------------------------------------------------------------
//                                     DECLARATIONS
//-----------------------------------------------------------------------------
var express = require('express');
var router = express.Router();
//-----------------------------------------------------------------------------
//                                     PROGRAM
//-----------------------------------------------------------------------------
router.get('/:id', function(req, res, next) {
  res.send(
    'req.params=' + JSON.stringify(req.params, null, 2) + ', ' +
    'req.query=' + JSON.stringify(req.query, null, 2)
  );
});
//-----------------------------------------------------------------------------
//                                     EXPORTS
//-----------------------------------------------------------------------------
module.exports = router;
//-----------------------------------------------------------------------------
//                                     END
//-----------------------------------------------------------------------------
